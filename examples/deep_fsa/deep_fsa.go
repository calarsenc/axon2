// New creates new blank elements and initializes defaults
func (ss *Sim) New() {
	ss.Net = &deep.Network{}
	ss.TrnEpcLog = &etable.Table{}
	ss.TstEpcLog = &etable.Table{}
	ss.TstTrlLog = &etable.Table{}
	ss.TstCycLog = &etable.Table{}
	ss.RunLog = &etable.Table{}
	ss.RunStats = &etable.Table{}
	ss.ErrLrMod.Defaults()
	ss.ErrLrMod.Base = 0.2
	ss.ErrLrMod.Range.Set(0.2, 0.8)
	ss.PAlphaPlus = 0
	ss.Params = ParamSets
	ss.RndSeed = 1
	ss.ViewOn = true
	ss.TrainUpdt = axon.AlphaCycle
	ss.TestUpdt = axon.Cycle
	ss.TestInterval = 500
	ss.LayStatNms = []string{"InputP", "Hidden"}
	if InputNameMap == nil {
		InputNameMap = make(map[string]int, len(InputNames))
		for i, nm := range InputNames {
			InputNameMap[nm] = i
		}
	}
}

