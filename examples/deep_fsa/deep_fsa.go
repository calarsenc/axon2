// ThetaCyc runs one theta cycle (200 msec) of processing.
// External inputs must have already been applied prior to calling,
// using ApplyExt method on relevant layers (see TrainTrial, TestTrial).
// If train is true, then learning DWt or WtFmDWt calls are made.
// Handles netview updating within scope, and calls TrainStats()
func (ss *Sim) ThetaCyc(train bool) {
	// ss.Win.PollEvents() // this can be used instead of running in a separate goroutine
	viewUpdt := ss.TrainUpdt
	if !train {
		viewUpdt = ss.TestUpdt
	}

	// update prior weight changes at start, so any DWt values remain visible at end
	// you might want to do this less frequently to achieve a mini-batch update
	// in which case, move it out to the TrainTrial method where the relevant
	// counters are being dealt with.
	if train {
		ss.Net.WtFmDWt()
	}

	minusCyc := 150
	plusCyc := 50
	didAlphaPlus := false
	_ = didAlphaPlus

	ss.Net.NewState()
	ss.Time.NewState()
	for cyc := 0; cyc < minusCyc; cyc++ { // do the minus phase
		ss.Net.Cycle(&ss.Time)
		if !train {
			ss.LogTstCyc(ss.TstCycLog, ss.Time.Cycle)
		}
		ss.Time.CycleInc()
		switch ss.Time.Cycle { // save states at beta-frequency -- not used computationally
		case 75:
			ss.Net.ActSt1(&ss.Time)
			if train && erand.BoolProb(float64(ss.PAlphaPlus), -1) {
				didAlphaPlus = true
				ss.Time.PlusPhase = true // turns on drivers
			} else {
				didAlphaPlus = false
			}
		case 100:
			ss.Net.ActSt2(&ss.Time)
			ss.Time.PlusPhase = false // turns off drivers
			// if didAlphaPlus {         // this is not good...
			// 	ss.Net.CTCtxt(&ss.Time) // update context..
			// }
		}
		if cyc == minusCyc-1 { // do before view update
			ss.Net.MinusPhase(&ss.Time)
		}
		if ss.ViewOn {
			ss.UpdateViewTime(train, viewUpdt)
		}
	}
	ss.Time.NewPhase()
	if viewUpdt == axon.Phase {
		ss.UpdateView(train)
	}
	for cyc := 0; cyc < plusCyc; cyc++ { // do the plus phase
		ss.Net.Cycle(&ss.Time)
		if !train {
			ss.LogTstCyc(ss.TstCycLog, ss.Time.Cycle)
		}
		ss.Time.CycleInc()
		if cyc == plusCyc-1 { // do before view update
			ss.Net.PlusPhase(&ss.Time) // auto calls CTCtxt
		}
		if ss.ViewOn {
			ss.UpdateViewTime(train, viewUpdt)
		}
	}

	ss.TrialStats(train)

	if train {
		// ss.ErrLrMod.LrateMod(ss.Net.AsAxon(), float32(ss.TrlErr))
		ss.Net.DWt()
	}

	if viewUpdt == axon.Phase || viewUpdt == axon.AlphaCycle || viewUpdt == axon.ThetaCycle {
		ss.UpdateView(train)
	}

	if ss.TstCycPlot != nil && !train {
		ss.TstCycPlot.GoUpdate() // make sure up-to-date at end
	}
}

// ApplyInputs applies input patterns from given envirbonment.
// It is good practice to have this be a separate method with appropriate
// args so that it can be used for various different contexts
// (training, testing, etc).
func (ss *Sim) ApplyInputs(en env.Env) {
	ss.Net.InitExt() // clear any existing inputs -- not strictly necessary if always
	// going to the same layers, but good practice and cheap anyway

	// just using direct access to state here
	in := ss.Net.LayerByName("Input").(axon.AxonLayer).AsAxon()
	trg := ss.Net.LayerByName("Targets").(axon.AxonLayer).AsAxon()
	clrmsk, setmsk, _ := in.ApplyExtFlags()
	fsenv := en.(*FSAEnv)
	ns := fsenv.NNext.Values[0]
	for ni := range in.Neurons {
		inr := &in.Neurons[ni]
		inr.ClearMask(clrmsk)
		inr.SetMask(setmsk)
	}
	for i := 0; i < ns; i++ {
		lbl := fsenv.NextLabels.Values[i]
		li, ok := InputNameMap[lbl]
		if !ok {
			log.Printf("Input label: %v not found in InputNames list of labels\n", lbl)
			continue
		}
		if i == 0 {
			inr := &in.Neurons[li]
			inr.Ext = 1
			inr.ClearMask(clrmsk)
			inr.SetMask(setmsk)
		}
		inr := &trg.Neurons[li]
		inr.Ext = 1
		inr.ClearMask(clrmsk)
		inr.SetMask(setmsk)
	}
}

// TrainTrial runs one trial of training using TrainEnv
func (ss *Sim) TrainTrial() {
	if ss.NeedsNewRun {
		ss.NewRun()
	}

	ss.TrainEnv.Step() // the Env encapsulates and manages all counter state

	// Key to query counters FIRST because current state is in NEXT epoch
	// if epoch counter has changed
	epc, _, chg := ss.TrainEnv.Counter(env.Epoch)
	if chg {
		ss.LogTrnEpc(ss.TrnEpcLog)
		ss.TrainEnv.Trial.Cur = 0
		if ss.ViewOn && ss.TrainUpdt > axon.AlphaCycle {
			ss.UpdateView(true)
		}
		if ss.TestInterval > 0 && epc%ss.TestInterval == 0 { // note: epc is *next* so won't trigger first time
			ss.TestAll()
		}
		if epc >= ss.MaxEpcs || (ss.NZeroStop > 0 && ss.NZero >= ss.NZeroStop) {
			// done with training..
			ss.RunEnd()
			if ss.TrainEnv.Run.Incr() { // we are done!
				ss.StopNow = true
				return
			} else {
				ss.NeedsNewRun = true
				return
			}
		}
	}

	ss.ApplyInputs(&ss.TrainEnv)
	ss.ThetaCyc(true) // train
}

// RunEnd is called at the end of a run -- save weights, record final log, etc here
func (ss *Sim) RunEnd() {
	ss.LogRun(ss.RunLog)
	if ss.SaveWts {
		fnm := ss.WeightsFileName()
		fmt.Printf("Saving Weights to: %v\n", fnm)
		ss.Net.SaveWtsJSON(gi.FileName(fnm))
	}
}

// NewRun intializes a new run of the model, using the TrainEnv.Run counter
// for the new run value
func (ss *Sim) NewRun() {
	run := ss.TrainEnv.Run.Cur
	ss.TrainEnv.Init(run)
	ss.TestEnv.Init(run)
	ss.Time.Reset()
	ss.Net.InitWts()
	ss.InitStats()
	ss.TrnEpcLog.SetNumRows(0)
	ss.TstEpcLog.SetNumRows(0)
	ss.NeedsNewRun = false
}

// InitStats initializes all the statistics, especially important for the
// cumulative epoch stats -- called at start of new run
func (ss *Sim) InitStats() {
	// accumulators
	ss.SumErr = 0
	ss.SumUnitErr = 0
	ss.SumCosDiff = 0
	ss.FirstZero = -1
	ss.NZero = 0
	// clear rest just to make Sim look initialized
	ss.TrlErr = 0
	ss.TrlUnitErr = 0
	ss.EpcUnitErr = 0
	ss.EpcPctErr = 0
	ss.EpcCosDiff = 0
}

// TrialStats computes the trial-level statistics and adds them to the epoch accumulators if
// accum is true.  Note that we're accumulating stats here on the Sim side so the
// core algorithm side remains as simple as possible, and doesn't need to worry about
// different time-scales over which stats could be accumulated etc.
// You can also aggregate directly from log data, as is done for testing stats
func (ss *Sim) TrialStats(accum bool) {
	inp := ss.Net.LayerByName("InputP").(axon.AxonLayer).AsAxon()
	trg := ss.Net.LayerByName("Targets").(axon.AxonLayer).AsAxon()
	ss.TrlCosDiff = float64(inp.CosDiff.Cos)
	err := 0.0
	thr := float32(0.3)
	gotOne := false
	for ni := range inp.Neurons {
		inn := &inp.Neurons[ni]
		if inn.IsOff() {
			continue
		}
		tgn := &trg.Neurons[ni]
		if tgn.Ext > 0.5 {
			if inn.ActM > thr {
				gotOne = true
			}
		} else {
			if inn.ActM > thr {
				err += float64(inn.ActM)
			}
		}
	}
	if !gotOne {
		err += 1
	}
	ss.TrlUnitErr = err
	if ss.TrlUnitErr > 0 {
		ss.TrlErr = 1
	} else {
		ss.TrlErr = 0
	}
	if accum {
		ss.SumErr += ss.TrlErr
		ss.SumUnitErr += ss.TrlUnitErr
		ss.SumCosDiff += ss.TrlCosDiff
	}
}

// TrainEpoch runs training trials for remainder of this epoch
func (ss *Sim) TrainEpoch() {
	ss.StopNow = false
	curEpc := ss.TrainEnv.Epoch.Cur
	for {
		ss.TrainTrial()
		if ss.StopNow || ss.TrainEnv.Epoch.Cur != curEpc {
			break
		}
	}
	ss.Stopped()
}

// TrainRun runs training trials for remainder of run
func (ss *Sim) TrainRun() {
	ss.StopNow = false
	curRun := ss.TrainEnv.Run.Cur
	for {
		ss.TrainTrial()
		if ss.StopNow || ss.TrainEnv.Run.Cur != curRun {
			break
		}
	}
	ss.Stopped()
}

// Train runs the full training from this point onward
func (ss *Sim) Train() {
	ss.StopNow = false
	for {
		ss.TrainTrial()
		if ss.StopNow {
			break
		}
	}
	ss.Stopped()
}

// Stop tells the sim to stop running
func (ss *Sim) Stop() {
	ss.StopNow = true
}

